package es.pentagono;

public class Metadata {
    public String title;
    public String author;
    public String date;
    public String releaseDate;

    public Metadata(String title, String author, String date, String releaseDate) {
        this.title = title;
        this.author = author;
        this.date = date;
        this.releaseDate = releaseDate;
    }
}
